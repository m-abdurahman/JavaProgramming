package day07_arithmeticOperators_casting;

public class WarmUpClass {
    public static void main(String[] args){
        String city1= "Chicago";
        String city2= "Atlanta";
        float ticketPrice= 216.80F;
        System.out.println("From " + city1 + " to " + city2 + " is $"
                + ticketPrice);

        String sentence= "From " + city1 + " to " + city2 + " is $"
                + ticketPrice;
        System.out.println(sentence);
    }
}
