package day33_arrays;

public class GroupFriends {
    public static void main(String[] args) {
        String [] friends = {"Brad", "Lynn", "Victor", "Anna", "Beny", "Oz", "Al", "Mana", "Frejus"};
        for (String eachFriends : friends){
            System.out.println("Happy Holidays " + eachFriends + "!");
        }
    }
}
